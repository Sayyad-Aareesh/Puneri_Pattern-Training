package com.basic;

public class Datatypes {

	public static void main(String[] args) {
		System.out.println("Java Primitive Datatypes Program");
		byte b =127;
		short s=2031;
		int i =10;
		long l = 123456789L;
		float f = 20.34f;
		double d =34.567;
		char c ='@';
		boolean bool=true;
		
		System.out.println();
		System.out.println("_______________________________________________________");
		System.out.println();
		
		System.out.println("Value of byte data b : "+b);
		System.out.println("Value of Short data s : "+s);
		System.out.println("Value of int data i : "+i);
		System.out.println("Value of long data l : "+l);
		System.out.println("Value of float data f : "+f);
		System.out.println("Value of double data d : "+d);
		System.out.println("Value of char data c : "+c);
		System.out.println("Value of boolean data bool : "+bool);
		 
		

	}

}
