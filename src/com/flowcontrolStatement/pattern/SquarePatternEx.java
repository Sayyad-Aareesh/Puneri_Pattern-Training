package com.flowcontrolStatement.pattern;

public class SquarePatternEx {

	public static void main(String[] args) {
		int i,j;
//		program to print Square pattern
		System.out.println("program to print Square pattern");
		//System.out.println();
		for ( i = 1; i <= 3; i++) {
			for(j =1 ;j<=3;j++)
			{
				System.out.print("*");
			}
			System.out.println();
		}

	}

}

